            pst.setString(14, prev_degree2.getText());
            pst.setString(15, prev_degree3.getText());
            pst.setString(16, prev_degree4.getText());
            pst.setString(17, input_additional.getText());
            
            
            pst.setString(18, input_job1.getText()); //panel3
            pst.setString(19, input_job2.getText());
            pst.setString(20, input_job3.getText());
            pst.setString(21, input_job4.getText());          
           
